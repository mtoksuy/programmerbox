</pre>
</code>

<div class="html_tab">10-master.conf</div>
<pre class="brush: html; light:true; tab-size: 2;">
Postfixからの認証要求の受付
#unix_listener /var/spool/postfix/private/auth {
#  mode = 0666
#}
↓
unix_listener /var/spool/postfix/private/auth {
 mode = 0660
 user = postfix
 group = postfix
}
</pre>

<p class="m_t_60 m_0">一通り設定が完了しましたらdovecotの起動と自動起動onにましょう。</p>

