変更する点は2点です。

&lt;Directory /usr/share/phpMyAdmin/&gt;
   &lt;IfModule mod_authz_core.c&gt;
     # Apache 2.4
     &lt;RequireAny&gt;
       Require ip 127.0.0.1
       Require ip ::1
     &lt;/RequireAny&gt;
   &lt;/IfModule&gt;
   &lt;IfModule !mod_authz_core.c&gt;
     # Apache 2.2
     Order Deny,Allow
     Deny from All
     Allow from 127.0.0.1
     Allow from ::1
   &lt;/IfModule&gt;
&lt;/Directory&gt;
↓
&lt;Directory /usr/share/phpMyAdmin/&gt;
   &lt;IfModule mod_authz_core.c&gt;
     # Apache 2.4
     &lt;RequireAny&gt;
       Require ip 127.0.0.1
       Require ip ::1
     &lt;/RequireAny&gt;
   &lt;/IfModule&gt;
   &lt;IfModule !mod_authz_core.c&gt;
     # Apache 2.2
     Order Deny,Allow
     Deny from All
     Allow from All
   &lt;/IfModule&gt;
&lt;/Directory&gt;

Allow from 127.0.0.1
↓
Allow from All
に変更して

Allow from ::1
を削除して下さい。

これで全てのバーチャルホストで参照出来るようになります。
</pre>



<p class="m_t_30 m_0">次はログイン画面で認証する時にどの認証にするかの設定を行います。デフォルトだとベーシック認証になっていますのでここではクッキー認証に切り替えたいと思います。</p>


<code class="command">
<pre>
vi /etc/phpMyAdmin/config.inc.php
</pre>
</code>



<div class="html_tab">config.inc.php</div>
<pre class="brush: html; light:true; tab-size: 2;">
$cfg['Servers'][$i]['auth_type']     = 'http';
↓
$cfg['Servers'][$i]['auth_type']     = 'cookie';

これでcookie認証に切り替わります。
</pre>

<p class="m_t_30 m_0">これでphpMyAdminのインストールと設定は完了です。最後にhhtpdを再起動して設定を読み込みをさせましょう。</p>


<code class="command">
<pre>
service httpd restart
</pre>
</code>

<p>以上でphpMyAdminの章を終わりにさせて頂きます。</p>

