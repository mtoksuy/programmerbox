<h2 class="heading">パケットフィルタリングの設定をする</h2>
<p class="m_0">ファイヤーウォールを設定ですね、使用するポートだけの許可するようにしました。ここでファイヤーウォールの設定を一気に済ましときましょう。</p>

<code class="command">
<pre>
//----------------------
//ファイヤーウォール設定
//----------------------
// 現在の設定の確認
iptables -L

// FORWARDは使わない
iptables -P FORWARD DROP

// まずは全許可に
iptables -P INPUT ACCEPT

// デフォルトのルールを全て削除
iptables -F

// 自分自身からのパケットは全許可
iptables -A INPUT -i lo -j ACCEPT

// SSH許可 or SSH自分(255.255.255.255)だけ許可
iptables -A INPUT -p tcp --dport 22 -j ACCEPT
 or
iptables -A INPUT -s 255.255.255.255 -p tcp --dport 22 -j ACCEPT

// FTPの接続を許可
iptables -A INPUT -p tcp --dport 20 -j ACCEPT
iptables -A INPUT -p tcp --dport 21 -j ACCEPT
iptables -A INPUT -p tcp --dport 60000:60030 -j ACCEPT

// SMTP(送信メール)の許可
iptables -A INPUT -p tcp --dport 25 -j ACCEPT

// DNS関係
iptables -A INPUT -p tcp --dport 53 -j ACCEPT
iptables -A INPUT -p udp --dport 53 -j ACCEPT

// http, https の接続を許可
iptables -A INPUT -p tcp --dport 80 -j ACCEPT
iptables -A INPUT -p tcp --dport 443 -j ACCEPT

// POP(受信メール)の許可
iptables -A INPUT -p tcp --dport 110 -j ACCEPT

// NTPサーバの通信許可
iptables -A INPUT -p tcp --dport 123 -j ACCEPT

// SUBMISSION ポートを開放
iptables -A INPUT -p tcp --dport 587 -j ACCEPT

// imaps
iptables -A INPUT -p tcp --dport 993 -j ACCEPT

// pop3s
iptables -A INPUT -p tcp --dport 995 -j ACCEPT

// PINGを許可
iptables -A INPUT -p icmp -j ACCEPT

// TCPの接続開始と応答、FTPデータなどを許可
iptables -A INPUT -m state --state ESTABLISHED,RELATED -j ACCEPT

// 設定したルール以外のパケットを拒否
iptables -P INPUT DROP

// 設定セーブ
service iptables save

