<h2 class="heading">開発期間は10日(7割はテストで時間を食っています)</h2>

<p>根幹システムに限っていえばタイムリーでプログラマーボックスを再構築したばかりでしたので、それをそのまんま流用したので3時間くらいでしょうか。</p>

